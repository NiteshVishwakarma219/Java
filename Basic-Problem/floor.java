// Floor --> Floor means rounding down to the nearest integer.
// In Java, you can use the Math.floor method to achieve this.

public class floor {
    public static void main(String[] args) {
        double num1 = 7.8;
        double num2 = -7.8;

        // Using Math.floor
        System.out.println("Math.floor(" + num1 + ") = " + Math.floor(num1));
        System.out.println("Math.floor(" + num2 + ") = " + Math.floor(num2));
    }
}
