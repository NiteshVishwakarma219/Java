// Ceil --> ceil means going up to the nearest integer

public class ceil {
    public static void main(String[] args) {
        double num1 = 7.2;
        double num2 = -7.2;

        // Using Math.ceil
        System.out.println("Math.ceil(" + num1 + ") = " + Math.ceil(num1));
        System.out.println("Math.ceil(" + num2 + ") = " + Math.ceil(num2));
    }
}
