/*

Functions in Java-->

A function is a block of code that performs a specific task. It is a reusable piece of code that can be called from other parts of the program. Functions help in organizing code, improving readability, and reducing redundancy.

command line Arguments--->
Java command-line argument is an argument, i.e., passed at the time of running the Java program. 
Command-line arguments passed from the console can be received by the Java program and used as input.

 */

// Example of function
public class $9_Functions_or_Methods {
    // Function definition
    public static void greet() {
        System.out.println("Hello, welcome to Java functions!");  // work
    }

    public static void main(String[] args) {   // main function
        // Function call
        greet();  // Output: Hello, welcome to Java functions!
    }
}

