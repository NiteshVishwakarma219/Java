/*
  Identifiers in Java

   - Identifiers are names given to variables, methods, classes, and other entities in Java.
   - They must start with a letter (a-z, A-Z), underscore (_), or dollar sign ($).
   - Subsequent characters can be letters, digits (0-9), underscores, or dollar signs.
   - Identifiers are case-sensitive.
   - Reserved keywords (e.g., int, class, public) cannot be used as identifiers.
   - They should be meaningful and follow naming conventions (e.g., camelCase for variables and methods, PascalCase for classes).

 */
public class $2_Identifiers {
    public static void main(String[] args) {
        int _myVariable = 10; // Valid identifier
        int $anotherVariable = 20; // Valid identifier
        int myVariable2 = 30; // Valid identifier

        System.out.println("Value of _myVariable: " + _myVariable);
        System.out.println("Value of $anotherVariable: " + $anotherVariable);
        System.out.println("Value of myVariable2: " + myVariable2);
    }
  }